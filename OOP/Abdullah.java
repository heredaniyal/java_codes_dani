package OOP;

public class Abdullah {

    public static void getSum(int a, int b)
    {
        System.out.println(a + b);
    }

    public static void getSum(int a, int b, int c)
    {
        System.out.println(a + b + c);
    }

    public static void main(String[] args)
    {
        getSum(2,2);
        getSum(2,2,2);
    }
}
